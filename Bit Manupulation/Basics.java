
public class Basics {
    public static void main(String[] args) {
        System.out.println(5&6);// And
        System.out.println(8|2);//OR
        System.out.println(1^2);//XOR
        System.out.println(~5);// Negation
        System.out.println(~0);
        System.out.println(5<<2);//Left Shift for a<<b , ans= a*2^b
        System.out.println(6>>1);// Right Shift   a>>b , ans= a/2^b
    }
}
